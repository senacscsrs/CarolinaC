// 14. Dia da semana (1 a 7)
let diaSemana = 3; // altere para testar
switch (diaSemana) {
  case 1:
    console.log("Domingo");
    break;
  case 2:
    console.log("Segunda-feira");
    break;
  case 3:
    console.log("Terça-feira");
    break;
  case 4:
    console.log("Quarta-feira");
    break;
  case 5:
    console.log("Quinta-feira");
    break;
  case 6:
    console.log("Sexta-feira");
    break;
  case 7:
    console.log("Sábado");
    break;
  default:
    console.log("Número inválido! Digite de 1 a 7.");
}

// 15. Semáforo (vermelho, amarelo, verde)
let cor = "amarelo"; // altere para testar
switch (cor) {
  case "vermelho":
    console.log("Pare!");
    break;
  case "amarelo":
    console.log("Atenção, prepare-se para parar.");
    break;
  case "verde":
    console.log("Pode seguir.");
    break;
  default:
    console.log("Cor inválida! Use vermelho, amarelo ou verde.");
}

