// 1. Variáveis com nome, idade e se gosta de programação
let nome = "Carolina";
let idade = 15;
let gostaDeProgramacao = true;
console.log(nome, idade, gostaDeProgramacao);

// 2. Variável fruta mudando de valor
let fruta = "maçã";
console.log("Fruta inicial:", fruta);
fruta = "banana";
console.log("Fruta depois da mudança:", fruta);

// 3. Template string com nome e idade
let meuNome = "Carolina";
let minhaIdade = 20;
console.log(`Meu nome é ${meuNome} e tenho ${minhaIdade} anos`);

// 4. Concatenação com +
let cidade = "São Paulo";
let estado = "SP";
console.log("Eu moro em " + cidade + ", que fica no estado de " + estado + ".");

// 5. Template string com cidade e estado
console.log(`Eu moro em ${cidade}, que fica no estado de ${estado}.`);

// 6. console.log com vírgula
let produto = "Notebook";
let preco = 3500;
console.log("Produto:", produto, "Preço:", preco);

// 7. Quebra de linha com \n
let mensagem = "Primeira linha\nSegunda linha";
console.log(mensagem);

// 8. Símbolo especial com Unicode
let simbolo = "\u00A9";
console.log("Símbolo especial:", simbolo);
